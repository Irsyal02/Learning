def hitung_luas_persegi_panjang():

  panjang = float(input("Masukkan panjang persegi panjang: "))
  lebar = float(input("Masukkan lebar persegi panjang: "))

  luas = panjang * lebar
  print("Luas persegi panjang adalah:", luas)

hitung_luas_persegi_panjang()