def hitung_total_belanja():

  harga_satuan = float(input("Masukkan harga satuan barang: "))
  jumlah_barang = int(input("Masukkan jumlah barang yang dibeli: "))

  total_harga = harga_satuan * jumlah_barang

  print("Total harga belanja adalah:", total_harga)

hitung_total_belanja()